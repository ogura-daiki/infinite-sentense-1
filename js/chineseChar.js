const total = all.reduce((c, v) => c + v[0], 0);

const randomChar = () => {
  let charRand = Math.floor(Math.random() * total);
  for (const [num, char] of all) {
    if (charRand < num) return char;
    charRand -= num;
  }
};

export { randomChar };